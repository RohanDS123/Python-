# This is the program of taking the input from the user.

# Note - We use of input function in python but input function always take the user input as a string .

a = input("Enter the first number here: ")
b = input("Enter the second number here: ")
print(a + b)

# Now we should to convert this into the integer - 

print(int(a) + int(b))

# We can also do something like this -
print(float(a) + int(b))

